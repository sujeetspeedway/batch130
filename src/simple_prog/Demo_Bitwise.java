package simple_prog;

public class Demo_Bitwise {

	public static void main(String[] args) {
		
		int a = 5 , b = 3 ;
		
		System.out.println(a & b ); // AND
		
		System.out.println(a | b);  // OR    001 
		                             //      011
		
		System.out.println( a << 1); 
		
		System.out.println(a >> 1); 
		

	}

}
