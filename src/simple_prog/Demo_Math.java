package simple_prog;

public class Demo_Math {

	public static void main(String[] args) {
		
		System.out.println(Math.abs(-10)); // 10
		
		System.out.println(Math.max(5, 10));// 10 
		
		System.out.println(Math.sqrt(25)); // 5
		
		System.out.println(Math.max(50, 100));// 100
		
		System.out.println(Math.min(20,60));// 20
		
		System.out.println(Math.pow(2, 3));// 8
		
		System.out.println(Math.round(4.7)); // 5

	}

}
